package com.manito.bill.entry.eightSevenLottery;

public class LoginEntry {
    private String appLatform;
    private String appType;
    private String appVersion;
    private String credential;
    private String identity;
    private String type;
    private String umengchannel;

    public String getAppLatform() {
        return appLatform;
    }

    public void setAppLatform(String appLatform) {
        this.appLatform = appLatform;
    }

    public String getAppType() {
        return appType;
    }

    public void setAppType(String appType) {
        this.appType = appType;
    }

    public String getAppVersion() {
        return appVersion;
    }

    public void setAppVersion(String appVersion) {
        this.appVersion = appVersion;
    }

    public String getCredential() {
        return credential;
    }

    public void setCredential(String credential) {
        this.credential = credential;
    }

    public String getIdentity() {
        return identity;
    }

    public void setIdentity(String identity) {
        this.identity = identity;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getUmengchannel() {
        return umengchannel;
    }

    public void setUmengchannel(String umengchannel) {
        this.umengchannel = umengchannel;
    }
}
