package com.manito.bill.entry.eightSevenLottery.programme;

public class ProgrammeMatchInfo {
    private String guestTeam;
    private String homeTeam;
    private String leagueName;
    private String matchId;
    private String playTypes;
    private String raceNo;

    public String getGuestTeam() {
        return guestTeam;
    }

    public void setGuestTeam(String guestTeam) {
        this.guestTeam = guestTeam;
    }

    public String getHomeTeam() {
        return homeTeam;
    }

    public void setHomeTeam(String homeTeam) {
        this.homeTeam = homeTeam;
    }

    public String getLeagueName() {
        return leagueName;
    }

    public void setLeagueName(String leagueName) {
        this.leagueName = leagueName;
    }

    public String getMatchId() {
        return matchId;
    }

    public void setMatchId(String matchId) {
        this.matchId = matchId;
    }

    public String getPlayTypes() {
        return playTypes;
    }

    public void setPlayTypes(String playTypes) {
        this.playTypes = playTypes;
    }

    public String getRaceNo() {
        return raceNo;
    }

    public void setRaceNo(String raceNo) {
        this.raceNo = raceNo;
    }
}
